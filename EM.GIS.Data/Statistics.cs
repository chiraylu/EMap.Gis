namespace EM.GIS.Data
{
    /// <summary>
    /// 统计
    /// </summary>
    public class Statistics
    {
        /// <summary>
        /// 最大值
        /// </summary>
        public double Maximum { get; set; }
        /// <summary>
        /// 平均值
        /// </summary>
        public double Mean { get; set; }
        /// <summary>
        /// 最小值
        /// </summary>
        public double Minimum { get; set; }
        /// <summary>
        /// 标准差
        /// </summary>
        public double StdDeviation { get; set; }
    }
}