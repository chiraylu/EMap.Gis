namespace EMap.Gis.Symbology
{
    /// <summary>
    /// Determines if all parts should be labeled or just the largest.
    /// </summary>
    public enum PartLabelingMethod
    {
        /// <summary>
        /// Label all parts.
        /// </summary>
        LabelAllParts,

        /// <summary>
        /// Only label the largest part.
        /// </summary>
        LabelLargestPart
    }
}